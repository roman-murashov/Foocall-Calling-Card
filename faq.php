<div class="faq-answer gradient-grey">
<h3><a id="question-05">Can I call an access number from any phone?</a></h3>
<p>Yes, you can call one of our access numbers from any phone. Which access number you use may depend on where you are calling from.</p>
<p>Your phone provider may charge you for calling an access number although we provide access numbers which are typically included in inclusive minutes allowances.</p>
