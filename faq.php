<p>There’s no need to add more credit to your Foocall Card. Once your credit is used up you simply buy a new card from your nearest retailer. <a href="where-to-buy.php">Find out where FooCall International Calling cards are available</a>.</p>
<p class="back-to-top"><a href="#list">^ back to list</a></p>
</div>

<div class="faq-answer gradient-grey">
<h3><a id="question-08">How good is the call quality and reliability?</a></h3>
<p>Very good - what you would expect from making a call using your home phone. As well as providing really low call rates we know the importance of a reliable service to our customers. 
It's worth noting though, that no matter how you call, sometimes the destination you are calling may not always be able to provide really great call quality - this is due to the telecommunications infrastructure in some countries around the world being of different standards.</p>
