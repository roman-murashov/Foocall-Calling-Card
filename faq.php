</div>

<div class="faq-answer gradient-grey">
<h3><a id="question-03">Which countries can I call to?</a></h3>
<p>You can use a FooCall International Calling Card to call all over the world. The card can be used to call landlines and mobile and other numbers, however, certain number types such as premium rate numbers cannot be called. This is for both your and our protection.</p>
<p class="back-to-top"><a href="#list">^ back to list</a></p>
