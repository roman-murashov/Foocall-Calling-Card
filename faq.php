<div class="faq-answer gradient-grey">
<h3><a id="question-02">How do I activate my FooCall Card?</a></h3>
<p>When you purchase a card, the retailer will activate your card for you at the till.
When you're ready to use your card you’ll need to scratch off the panel on the back of the card to reveal your PIN. When you call one of the access numbers you will then be prompted to enter your PIN.</p>
